/*
    Debouncing ensures that a function is called only after a certain amount of time has passed since the last time it was called.
    It is used to ensure that the function is not called more than once in a given time frame, even if the function is called multiple times
    
    Debouncing is a programming practice used to ensure that time-consuming tasks do not fire so often, that it stalls the performance of the web page. In other words, it limits the rate at which a function gets invoked.
*/